// Concatenation
// Concatenation is the process of combining multiple files into one.

let hi = "Hello";
let world = "World";
let concatenation = hi + " " + world;
console.log(concatenation);

let number = "10";
let phrase = hi.concat(" " + number);
console.log(phrase);

let firstName = "Matias"
let lastName = "Gonzalez";
let fullName = firstName.concat(" " + lastName);

// Back ticks are used to concatenate strings
console.log(`Hello ${fullName}!`);	